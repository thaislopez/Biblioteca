    private void txtPrecioFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtPrecioFocusLost
        if(!this.validaciones.esPrecio(this.txtPrecio.getText())) {
            this.txtPrecio.setBackground(Color.red);
            this.precio = -1.1;
        }else {
            this.txtPrecio.setBackground(Color.white);
            this.txtPrecio.setText(this.txtPrecio.getText().replace(',', '.'));
            this.precio = Double.parseDouble(this.txtPrecio.getText());
            this.txtPrecio.setText(Double.toString(precio));
        }
    }//GEN-LAST:event_txtPrecioFocusLost

