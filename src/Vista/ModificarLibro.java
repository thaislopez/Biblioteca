    private boolean datosCorrectos() {
        boolean validado = false;
        
        this.descripcion = this.txtDescripcion.getText();
        this.proveedor = this.txtProveedor.getText();
        this.editorial = this.txtEditorial.getText();
        this.autor = this.txtAutor.getText();

        if (this.anioEdicion < 0 || this.precio < 0.0 || this.inventario < 0
                || this.descripcion.isEmpty() || this.proveedor.isEmpty()
                || this.editorial.isEmpty() || this.autor.isEmpty()) {
            this.lbAlerta.setVisible(true);
        } else {
            validado = true;
            this.lbAlerta.setVisible(false);
        }
        
        return validado;
    }
