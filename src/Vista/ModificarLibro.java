    private void txtAnioEdicionFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtAnioEdicionFocusLost
        int number;
        
        if(validaciones.esNumerico(this.txtAnioEdicion.getText())) {
            number = Integer.parseInt(this.txtAnioEdicion.getText()) * 1;
            this.txtAnioEdicion.setText(Integer.toString(number));
        }
        
        if(!validaciones.esAnioValido(this.txtAnioEdicion.getText())) {
            this.txtAnioEdicion.setBackground(Color.red);
            this.anioEdicion = -1;
        }else {
            this.txtAnioEdicion.setBackground(Color.white);
            this.anioEdicion = Integer.parseInt(this.txtAnioEdicion.getText());
        }
    }//GEN-LAST:event_txtAnioEdicionFocusLost

